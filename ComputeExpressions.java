public class ComputeExpressions {
    public static void main(String[] args) {
        double num1, num2, num3, num4, num5, num6;
        num1 = 9.5;
        num2 = 4.5;
        num3 = 2.5;
        num4 = 3.0;
        num5 = 45.5;
        num6 = 3.5;
        double result = (num1 * num2) - (num3 * num4) / (num5  - num6);
        System.out.println(result);
    }
}
